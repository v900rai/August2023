package august2023Target200;

public class pattern2 
{
	public static void main(String []args)
	{
		System.out.println("Print the Pattern program");
		int num=5;
		for(int i=1; i<=num; i++)
		{
			for(int j=1; j<=i; j++ )
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}
